    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAddActionPerformed
      // TODO add your handling code here: nut them
      if (selected_good.getId()!=null){
        JOptionPane.showMessageDialog(this, "Bạn đang chọn một sản phẩm rồi, bấm Refresh sau đó nhập lại thông tin mới!", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
      }
      else{
        Good good = new Good();
        good.setShop_id(txtMaSP.getText());
        good.setName(txtTenSP.getText());
        good.setQuantity(0);
        if (!txtPrice1.getText().matches("[0-9]+")) {
          JOptionPane.showMessageDialog(this, "Giá hàng nhập sai", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
          return;
        }
        good.setPrice(Integer.parseInt(txtPrice1.getText()));
        try {
          good_controller.createNewGood(good);
        } catch (ExecutionException | InterruptedException e) {
          // TODO Auto-generated catch block
          e.printStackTrace();
        }
      reset();
      }
    }// GEN-LAST:event_btnAddActionPerformed

