    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnFindActionPerformed
      // TODO add your handling code here:
      String find_text = txtFind.getText();
      if (!(find_text.isBlank())) {
        modelSP.setRowCount(0);
        try {
          good_list = good_controller.getAllGoods();
        } catch (ExecutionException | InterruptedException e) {
          System.out.println("error");
        }
        for (Good good : good_list) {
          if (good.getName().contains(find_text) || good.getShop_id().contains(find_text)){
            modelSP.addRow(new Object[] {
                TableResultSP.getRowCount() + 1,
                good.getShop_id(),
                good.getName(),
                good.getQuantity(),
                good.getPrice()
            });
          }
        }
      } else {
        JOptionPane.showMessageDialog(this, "Hãy nhập thông tin cần tìm kiếm!", "Lỗi",
            JOptionPane.INFORMATION_MESSAGE);
      }
    }                                       

